package cn.com.infosec.netcert.caAdmin.ui.template.templateUtils;

public class ExtensionEntry {
	private String oid;
	private String name;
	private String type;
	private String value;
	private String appendbasedn;
	private boolean isca;
	private String encoding;
	private Extension extension;

	public Extension getExtension() {
		return extension;
	}

	public void setExtension(Extension extension) {
		this.extension = extension;
	}

	public String getOid() {
		return oid;
	}

	public void setOid(String oid) {
		this.oid = oid;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public String getType() {
		return type;
	}

	public void setType(String type) {
		this.type = type;
	}

	public String getValue() {
		return value;
	}

	public void setValue(String value) {
		this.value = value;
	}

	public String getAppendbasedn() {
		return appendbasedn;
	}

	public void setAppendbasedn(String appendbasedn) {
		this.appendbasedn = appendbasedn;
	}

	public boolean isIsca() {
		return isca;
	}

	public void setIsca(boolean isca) {
		this.isca = isca;
	}

	public String getEncoding() {
		return encoding;
	}

	public void setEncoding(String encoding) {
		this.encoding = encoding;
	}
	


}
